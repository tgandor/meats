/*
 * Skip the comment - manually without matching
 * ---
 * Pominicie komentarza bez stosowania DAS
 */
static void
skip_comment()
{
	int c1, c2;

	offset += 2;  // for the /*
	column += 2;  // znaki /*

	c1 = input();
	c2 = input();

	while (c2 != EOF && !(c1 == '*' && c2 == '/'))
	{
		if (c1 == '\n')
		{
			++lineno;
			column = 0;
		}
		else
		    ++column;
		++offset;
		c1 = c2;
		c2 = input();
	}
	++column;
	++offset;
}

/*
Filter single-character tokens and return them as symbolic names
---
Filtrowanie tokenw, ktre s jednoznakowe i zwracanie ich jako kodw symbolicznych
*/
int handle_single_char(int c)
{
  offset ++;
  column ++;
  switch(c)
  {
    /*
	Atoms which are not interesting
	---
	Atomy, ktre nie s ciekawe
	*/
	case ';':
	  atcMetrics.semicolons++;
	case ',':
	  return -1;
	/*
	Interesting atoms
	---
	Atomy, ktre s ciekawe
	*/
	case '[':
	case ']':
	case '(':
	case ')':
	  return _ATOM(BRACKET);
	case '.':
	  return _ATOM(SELECTOR);
	case '{':
	  return _ATOM(BEGN);
	case '}':
	  return _ATOM(END);
	case '+':
	  atcMetrics.plus++;
	  return _ATOM(ARITHMETIC);
	case '-':
	  atcMetrics.minus++;
	  return _ATOM(ARITHMETIC);
	case '*':
	  atcMetrics.times++;
	case '/':
	case '%':
	  return _ATOM(ARITHMETIC);
	case '^':
	case '&':
	case '|':
	case '~':
	  return _ATOM(BITWISE);
	case '!':
	case '?':
	case ':':
	  return _ATOM(LOGIC);
	case '<':
	case '>':
	  return _ATOM(RELATION);
	case '=':
	  atcMetrics.assignments++;
	  return _ATOM(ASSIGNMENT);
	/*
	Atoms we don't really know or distinct
	---
	Atomy, ktrych nie znamy lub generalizujemy
	*/
	default:
	  return _ATOM(OTHER);
  }
  return _ATOM(IMPOSSIBLE);
}

int handle_token(int t)
{
  offset += yyleng;
  column += yyleng;
  switch(t)
  {
    case ADDEQ:  /* "+=" */
	  atcMetrics.plus++;
	  atcMetrics.assignments++;
	  return _ATOM(ASSIGNMENT);
    case SUBEQ:  /* "-=" */
	  atcMetrics.minus++;
	  atcMetrics.assignments++;
	  return _ATOM(ASSIGNMENT);
    case MULEQ:  /* "*=" */
	  atcMetrics.assignments++;
	  atcMetrics.times++;
	  return _ATOM(ASSIGNMENT);
    case DIVEQ:  /* "/=" */
    case MODEQ:  /* "%=" */
    case XOREQ:  /* "^=" */
    case ANDEQ:  /* "&=" */
    case OREQ:   /* "|=" */
	  atcMetrics.assignments++;
	  return _ATOM(ASSIGNMENT);
    case SL:     /* "<<" */
    case SR:     /* ">>" */
    case SLEQ:   /* "<<=" */
    case SREQ:   /* ">>=" */
	  return _ATOM(BITWISE);
    case ANDAND: /* "&&" */
    case OROR:   /* "||" */
	  return _ATOM(LOGIC);
    case PLUSPLUS:   /* "++" */
    case MINUSMINUS: /* "--" */
    case SIZEOF:     /* "sizeof" */
	  return _ATOM(ARITHMETIC);
    case ARROW:      /* "->" */
	  return _ATOM(SELECTOR);

    case FOR:       /* "for" */
    case CONTINUE:  /* "continue" */
    case GOTO:      /* "goto" */
    case IF:        /* "if" */
    case ELSE:      /* "else" */
    case WHILE:     /* "while" */
    case DO:        /* "do" */
    case SWITCH:    /* "switch" */
    case CASE:      /* "case" */
    case BREAK:     /* "break" */
    case DEFAULT:   /* "default" */
    case RETURN:    /* "return" */
	  return _ATOM(CONTROL);

    case CHAR:    /* "char" */
    case FLOAT:   /* "float" */
    case DOUBLE:  /* "double" */
    case INT:     /* "int" */
    case LONG:    /* "long" */
	  return _ATOM(BUILTINTYPE);
    case TYPEDEF: /* "typedef" */
    case UNION:   /* "union" */
    case ENUM:    /* "enum" */
    case STRUCT:  /* "struct" */
	  return _ATOM(DECL);

    case EQ:     /* "==" */
    case NOTEQ:  /* "!=" */
    case LTEQ:   /* "<=" */
    case GTEQ:   /* ">=" */
	  return _ATOM(RELATION);
	/*
	Tokens to be filtered out
	---
	Tokeny do odfiltrowania
	*/
    case SHORT:     /* "short" */
    case SIGNED:    /* "signed" */
    case STATIC:    /* "static" */
    case UNSIGNED:  /* "unsigned" */
    case ELLIPSIS:  /* "..." */
    case AUTO:      /* "auto" */
    case EXTERN:    /* "extern" */
    case VOLATILE:  /* "volatile" */
    case REGISTER:  /* "register" */
    case VOID:      /* "void" */
    case CONST:     /* "const" */
	  return -1;
	default:
	  return t;
  }
  return _ATOM(IMPOSSIBLE);
}

ATOM
construct_atom(int type)
{
  ATOM ret_atom;
  ret_atom = calloc(1, sizeof(struct atom));
  ret_atom->content = strdup(yytext);
  /*
  offset already updated by handle_token
  ---
  offset zosta ju zmodyfikowany w funkcji handle_token
  */
  ret_atom->start   = offset - yyleng;
  ret_atom->len     = yyleng;
  ret_atom->code    = type;
  ret_atom->line    = lineno;
  return ret_atom;
}

